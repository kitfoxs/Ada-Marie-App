import Foundation

public enum AdaMarieDeviceCommand: String, Codable, Sendable {
    case status = "device.status"
    case info = "device.info"
}

public enum AdaMarieBatteryState: String, Codable, Sendable {
    case unknown
    case unplugged
    case charging
    case full
}

public enum AdaMarieThermalState: String, Codable, Sendable {
    case nominal
    case fair
    case serious
    case critical
}

public enum AdaMarieNetworkPathStatus: String, Codable, Sendable {
    case satisfied
    case unsatisfied
    case requiresConnection
}

public enum AdaMarieNetworkInterfaceType: String, Codable, Sendable {
    case wifi
    case cellular
    case wired
    case other
}

public struct AdaMarieBatteryStatusPayload: Codable, Sendable, Equatable {
    public var level: Double?
    public var state: AdaMarieBatteryState
    public var lowPowerModeEnabled: Bool

    public init(level: Double?, state: AdaMarieBatteryState, lowPowerModeEnabled: Bool) {
        self.level = level
        self.state = state
        self.lowPowerModeEnabled = lowPowerModeEnabled
    }
}

public struct AdaMarieThermalStatusPayload: Codable, Sendable, Equatable {
    public var state: AdaMarieThermalState

    public init(state: AdaMarieThermalState) {
        self.state = state
    }
}

public struct AdaMarieStorageStatusPayload: Codable, Sendable, Equatable {
    public var totalBytes: Int64
    public var freeBytes: Int64
    public var usedBytes: Int64

    public init(totalBytes: Int64, freeBytes: Int64, usedBytes: Int64) {
        self.totalBytes = totalBytes
        self.freeBytes = freeBytes
        self.usedBytes = usedBytes
    }
}

public struct AdaMarieNetworkStatusPayload: Codable, Sendable, Equatable {
    public var status: AdaMarieNetworkPathStatus
    public var isExpensive: Bool
    public var isConstrained: Bool
    public var interfaces: [AdaMarieNetworkInterfaceType]

    public init(
        status: AdaMarieNetworkPathStatus,
        isExpensive: Bool,
        isConstrained: Bool,
        interfaces: [AdaMarieNetworkInterfaceType])
    {
        self.status = status
        self.isExpensive = isExpensive
        self.isConstrained = isConstrained
        self.interfaces = interfaces
    }
}

public struct AdaMarieDeviceStatusPayload: Codable, Sendable, Equatable {
    public var battery: AdaMarieBatteryStatusPayload
    public var thermal: AdaMarieThermalStatusPayload
    public var storage: AdaMarieStorageStatusPayload
    public var network: AdaMarieNetworkStatusPayload
    public var uptimeSeconds: Double

    public init(
        battery: AdaMarieBatteryStatusPayload,
        thermal: AdaMarieThermalStatusPayload,
        storage: AdaMarieStorageStatusPayload,
        network: AdaMarieNetworkStatusPayload,
        uptimeSeconds: Double)
    {
        self.battery = battery
        self.thermal = thermal
        self.storage = storage
        self.network = network
        self.uptimeSeconds = uptimeSeconds
    }
}

public struct AdaMarieDeviceInfoPayload: Codable, Sendable, Equatable {
    public var deviceName: String
    public var modelIdentifier: String
    public var systemName: String
    public var systemVersion: String
    public var appVersion: String
    public var appBuild: String
    public var locale: String

    public init(
        deviceName: String,
        modelIdentifier: String,
        systemName: String,
        systemVersion: String,
        appVersion: String,
        appBuild: String,
        locale: String)
    {
        self.deviceName = deviceName
        self.modelIdentifier = modelIdentifier
        self.systemName = systemName
        self.systemVersion = systemVersion
        self.appVersion = appVersion
        self.appBuild = appBuild
        self.locale = locale
    }
}
