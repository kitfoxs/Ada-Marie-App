import Foundation

public enum AdaMarieRemindersCommand: String, Codable, Sendable {
    case list = "reminders.list"
    case add = "reminders.add"
}

public enum AdaMarieReminderStatusFilter: String, Codable, Sendable {
    case incomplete
    case completed
    case all
}

public struct AdaMarieRemindersListParams: Codable, Sendable, Equatable {
    public var status: AdaMarieReminderStatusFilter?
    public var limit: Int?

    public init(status: AdaMarieReminderStatusFilter? = nil, limit: Int? = nil) {
        self.status = status
        self.limit = limit
    }
}

public struct AdaMarieRemindersAddParams: Codable, Sendable, Equatable {
    public var title: String
    public var dueISO: String?
    public var notes: String?
    public var listId: String?
    public var listName: String?

    public init(
        title: String,
        dueISO: String? = nil,
        notes: String? = nil,
        listId: String? = nil,
        listName: String? = nil)
    {
        self.title = title
        self.dueISO = dueISO
        self.notes = notes
        self.listId = listId
        self.listName = listName
    }
}

public struct AdaMarieReminderPayload: Codable, Sendable, Equatable {
    public var identifier: String
    public var title: String
    public var dueISO: String?
    public var completed: Bool
    public var listName: String?

    public init(
        identifier: String,
        title: String,
        dueISO: String? = nil,
        completed: Bool,
        listName: String? = nil)
    {
        self.identifier = identifier
        self.title = title
        self.dueISO = dueISO
        self.completed = completed
        self.listName = listName
    }
}

public struct AdaMarieRemindersListPayload: Codable, Sendable, Equatable {
    public var reminders: [AdaMarieReminderPayload]

    public init(reminders: [AdaMarieReminderPayload]) {
        self.reminders = reminders
    }
}

public struct AdaMarieRemindersAddPayload: Codable, Sendable, Equatable {
    public var reminder: AdaMarieReminderPayload

    public init(reminder: AdaMarieReminderPayload) {
        self.reminder = reminder
    }
}
